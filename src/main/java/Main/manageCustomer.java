    public void initTable () {
        
        model = (DefaultTableModel) jtableCustomer.getModel();
        model.setRowCount(0);
        ArrayList customer = Arrays.getAllCustomerRecord();
        int j = 0;
        
        for (int i = 0; i < customer.size()/7; i++) {
            model.insertRow(model.getRowCount(), new Object[] {
                customer.subList(j, j+7).get(0), customer.subList(j, j+7).get(1), customer.subList(j, j+7).get(2), customer.subList(j, j+7).get(3), customer.subList(j, j+7).get(4), customer.subList(j, j+7).get(5), customer.subList(j, j+7).get(6)
            });
            j+=7;
        }
        
    }
    
