    private void jtbnAddUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jtbnAddUpdateActionPerformed
        
        if (!validateInput()) {
            JOptionPane.showMessageDialog(null, "Empty Fields!");
        } else {
            try {
            int ID = Integer.parseInt(jtxtID.getText());
            String name = jtxtName.getText();
            int age = Integer.parseInt(jtxtAge.getText());
            String gender = jcmbGender.getSelectedItem().toString();
            String ic = jtxtIC.getText();
            String email = jtxtEmail.getText();
            int phone = Integer.parseInt(jtxtPhone.getText());

            Customer customer = new Customer(ID, name, age, gender, ic, email, phone);
            if (Arrays.getNewCustomerId() != ID) {

                if (customer.update(jtxtID.getText())) {
                    JOptionPane.showMessageDialog(null, "Succesfully Updated");
                } else {
                    JOptionPane.showMessageDialog(null, "Failed to Update");
                }
            } else {

                if (customer.add()) {
                    JOptionPane.showMessageDialog(null, "Succesfully Added");
                } else {
                    JOptionPane.showMessageDialog(null, "Failed to Add");
                }
            }

            reset();
            initTable();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Invalid Input!");
            }
        }
    }//GEN-LAST:event_jtbnAddUpdateActionPerformed

    private void jtbnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jtbnDeleteActionPerformed
        if (!validateInput()) {
            JOptionPane.showMessageDialog(null, "Please Select Customer to Delete");
        } else {
            if (new Customer().delete(jtxtID.getText())) {
                JOptionPane.showMessageDialog(null, "Succesfully Deleted");
                reset();
                initTable();
            } else {
                JOptionPane.showMessageDialog(null, "Failed to Delete");
            }
        }
    }//GEN-LAST:event_jtbnDeleteActionPerformed

