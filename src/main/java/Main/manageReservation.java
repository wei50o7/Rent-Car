    private void jbtnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnDeleteActionPerformed
        
        try {
            DefaultTableModel model = (DefaultTableModel)jtableReservation.getModel();
            int selectedRowIndex = jtableReservation.getSelectedRow();

            String ID = model.getValueAt(selectedRowIndex, 0).toString();
            if (new Reservation().delete(ID)) {
                JOptionPane.showMessageDialog(null, "Succesfully Deleted!");
                initTable();
            } else {
                JOptionPane.showMessageDialog(null, "Failed to Delete!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Please Select Record to Delete");
        }
    }//GEN-LAST:event_jbtnDeleteActionPerformed

    private void jbtnReturnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnReturnActionPerformed
        
        try {
            DefaultTableModel model = (DefaultTableModel)jtableReservation.getModel();
            int selectedRowIndex = jtableReservation.getSelectedRow();
            
            String ID = model.getValueAt(selectedRowIndex, 0).toString();
            Date endDate = new SimpleDateFormat("yyyy-MM-dd").parse(model.getValueAt(selectedRowIndex, 4).toString());
            String plate = model.getValueAt(selectedRowIndex, 1).toString();
            
            if (new Reservation().update(ID, endDate) && Car.returnCar(plate)) {
                JOptionPane.showMessageDialog(null, "Succesfully Returned");
            }
        } catch (ParseException ex) {
            Logger.getLogger(manageReservation.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Please Select Record to Return");
        }
        
        initTable();
        
    }//GEN-LAST:event_jbtnReturnActionPerformed

