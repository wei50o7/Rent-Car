    private void initTable () {
        
        model = (DefaultTableModel) jtableReservation.getModel();
        model.setRowCount(0);
        ArrayList reservation = Arrays.getAllReservationRecord();
        int j = 0;
        
        for (int i = 0; i < reservation.size()/10; i++) {
            model.insertRow(model.getRowCount(), new Object[] {
                reservation.subList(j, j+10).get(0), reservation.subList(j, j+10).get(1), reservation.subList(j, j+10).get(2), reservation.subList(j, j+10).get(3), reservation.subList(j, j+10).get(4), reservation.subList(j, j+10).get(5), reservation.subList(j, j+10).get(6), reservation.subList(j, j+10).get(7), reservation.subList(j, j+10).get(8), reservation.subList(j, j+10).get(9)
            });
            j+=10;
        }
        
    }
    
