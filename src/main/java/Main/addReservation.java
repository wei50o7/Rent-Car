    private void jtxtSearchKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtxtSearchKeyPressed
        modelCustomer = (DefaultTableModel)jtableCustomer.getModel();
        TableRowSorter<DefaultTableModel> trCustomer = new TableRowSorter<DefaultTableModel>(modelCustomer);
        jtableCustomer.setRowSorter(trCustomer);
        trCustomer.setRowFilter(RowFilter.regexFilter(jtxtSearch.getText().trim()));
        
        modelCar = (DefaultTableModel)jtableCar.getModel();
        TableRowSorter<DefaultTableModel> trCar = new TableRowSorter<DefaultTableModel>(modelCar);
        jtableCar.setRowSorter(trCar);
        trCar.setRowFilter(RowFilter.regexFilter(jtxtSearch.getText().trim()));
    }//GEN-LAST:event_jtxtSearchKeyPressed

