    private void initCustomerTable () {
        
        model = (DefaultTableModel) jtableCustomer.getModel();
        model.setRowCount(0);
        ArrayList customer = Arrays.getAllCustomerRecord();
        int j = 0;
        
        for (int i = 0; i < customer.size()/7; i++) {
            model.insertRow(model.getRowCount(), new Object[] {
                customer.subList(j, j+7).get(0), customer.subList(j, j+7).get(1), customer.subList(j, j+7).get(2), customer.subList(j, j+7).get(3), customer.subList(j, j+7).get(4), customer.subList(j, j+7).get(5), customer.subList(j, j+7).get(6)
            });
            j+=7;
        }
        
    }
    
    private void initCarTable () {
        
        model = (DefaultTableModel) jtableCar.getModel();
        model.setRowCount(0);
        ArrayList cars = Arrays.getAllCarRecord();
        int j = 0;
        
        for (int i = 0; i < cars.size()/10; i++) {
            
            if (cars.subList(j, j+10).get(8).equals("true")) {
                model.insertRow(model.getRowCount(), new Object[] {
                    cars.subList(j, j+10).get(0), cars.subList(j, j+10).get(1), cars.subList(j, j+10).get(2), cars.subList(j, j+10).get(3), cars.subList(j, j+10).get(4), cars.subList(j, j+10).get(5), cars.subList(j, j+10).get(6), cars.subList(j, j+10).get(7), cars.subList(j, j+10).get(9)
                    });
            }
            
            j+=10;
        }
            
    }
    
