    private void jbtnCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnCalculateActionPerformed
        
        if (jtxtCarPlate.getText().equals("") && jtxtCustName.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please Select Car and Customer Detail");
        } else if (jtxtCarPlate.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please Select Car Detail");
        } else if (jtxtCustName.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please Select Customer Detail");
        } else {
            try {
                if (!Reservation.isValid(jdcStart.getDate(), jdcEnd.getDate())) {
                    JOptionPane.showMessageDialog(null, "Invalid Date!");
                } else {
                    Long numberOfDays = Reservation.compare(jdcStart.getDate(), jdcEnd.getDate());
                    jtxtDuration.setText(numberOfDays.toString());

                    double price = parseDouble(jtxtCarRate.getText());
                    double total = price*numberOfDays;
                    double deposit = total * 0.30;

                    jtxtDeposit.setText(Double.toString(deposit));
                    jtxtTotal.setText(Double.toString(total));

                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Please Select End Date");
            }
        }
    }//GEN-LAST:event_jbtnCalculateActionPerformed

    private void jbtnReserveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnReserveActionPerformed
        
        if (jtxtCarPlate.getText().equals("") && jtxtCustName.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please Select Car and Customer Detail");
        } else if (jtxtCarPlate.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please Select Car Detail");
        } else if (jtxtCustName.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please Select Customer Detail");
        } else {
            
            if (jdcEnd.getDate() == null) {
                JOptionPane.showMessageDialog(null, "Please Select End Date");
            } else {
                jbtnCalculate.doClick();
                
                DefaultTableModel model = (DefaultTableModel)jtableCar.getModel();
                int selectedRowIndex = jtableCar.getSelectedRow();
                
                int ID = Arrays.getNewReservationId();
                String carID = model.getValueAt(selectedRowIndex, 0).toString();
                String plate = jtxtCarPlate.getText();
                String custName = jtxtCustName.getText();
                Date startDate = jdcStart.getDate();
                Date endDate = jdcEnd.getDate();
                int duration = Integer.parseInt(jtxtDuration.getText());
                double deposit = Double.parseDouble(jtxtDeposit.getText());
                double total = Double.parseDouble(jtxtTotal.getText());

                Reservation reservation = new Reservation(ID, plate, custName, startDate, endDate, duration, deposit, total);
                if (reservation.add() && Car.rentCar(carID)) {
                    JOptionPane.showMessageDialog(null, "Succesfully Added!");
                    reset();
                } else {
                    JOptionPane.showMessageDialog(null, "Failed to make Reservation");
                }
                new receipt(ID).setVisible(true);
            }
        }
        
    }//GEN-LAST:event_jbtnReserveActionPerformed

